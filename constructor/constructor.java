class constructor {

  public constructor(){
    System.out.println("引数はありません");
  }

  public constructor(String str1){
    System.out.println("引数で次の一つの値が入力されました。 →" + str1);
  }

  public constructor(String str1 , String str2){
    System.out.println("引数で次の二つつの値が入力されました。→ " + str1 + " & " + str2);
  }
  
}